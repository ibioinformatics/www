        <td><div align="justify"><b>The Human Proteome Map</b> (HPM - <a href="http://humanproteomemap.org/" target="_blank">http://humanproteomemap.org</a>)
            portal is an interactive resource to the scientific community by integrating the massive peptide sequencing result from the draft map of the human proteome project. The project was based on LC-MS/MS by utilizing of high resolution and high accuracy Fourier transform mass spectrometry. All mass spectrometry data including precursors and HCD-derived fragments were acquired on the Orbitrap mass analyzers in the high-high mode. Currently, the HPM contains direct evidence of translation of a number of protein products derived from over 17,000 human genes covering >84% of the annotated protein-coding genes in humans based on >290,000 non-redundant peptide identifications of multiple organs/tissues and cell types from individuals with clinically defined healthy tissues. This includes 17 adult tissues, 6 primary hematopoietic cells and 7 fetal tissues. The HPM portal provides visualization of the expression pattern of a given gene across the sampled tissues using the label-free quantitative proteomic data set in a simple graphical view. It also provides the highly sampled peptides for given protein through interactive visualization. In addition, the portal provides selected reaction monitoring (SRM) information for all peptides identified.</div></td>
      </tr>
