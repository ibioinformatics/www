			<div align="justify">
				<strong>Human Protein Reference Database</strong> 
					<a href="http://www.hprd.org" target="_blank"> (HPRD - http://www.hprd.org )</a>
					 is a centralized platform that stores and disseminates various information such as protein-protein interactions, post-translational modifications, tissue expression, sub-cellular localizations and enzyme-substrate relationships in normal condition. It stores these details for the currently known 30,047 human proteins. All information in this resource has been manually extracted from literature by expert biologists and reviewed by experienced researchers. HPRD also provides a tool called PhosphoMotif Finder (<a href="http://www.hprd.org/PhosphoMotif_finder" target="_blank">http://www.hprd.org/PhosphoMotif_finder</a>) to identify phosphorylation motifs which was developed using the manually curated motifs published in the literature.
