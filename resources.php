        <td align="center" valign="top">&#8226;</td>
        <td><div align="justify"><strong>Human Proteinpedia</strong> (<a href="http://www.humanproteinpedia.org/" target="_blank">http://www.humanproteinpedia.org/</a>) This resource is developed as a community portal for sharing and integrating human protein data derived from various high throughput as well as low throughput experiments. It allows the users to visualize the results of these experiments related to a protein of interest in a single page. This resource allows research labs around the world to contribute and maintain protein annotations derived from various platforms like mass spectrometry, immunochemistry and fluorescence based experiments.</div></td>
      </tr>

