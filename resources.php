      <tr>
        <td align="center" valign="top">&#8226;</td>
        <td><div align="justify"><strong>Plasma Proteome Database</strong> (<a href="http://www.plasmaproteomedatabase.org/" target="_blank">http://www.plasmaproteomedatabase.org/</a>)
            Plasma Proteome Database (PPD) was developed as a part of Human Proteome Organization's (HUPO) initial effort to characterize human plasma proteome. This is one of the largest resources on proteins reported in plasma and serum. PPD hosts qualitative and quantitative information on proteins (including those from MRM-based assays) reported in plasma and serum and hence serves as reference platform for biomarker discovery. In addition, proteins reported in extracellular vesicle isolated from plasma are also available. PPD query interface is designed to facilitate individual and batch queries using gene symbols, PubMed identifiers, RefSeq accessions, UniProt IDs or peptide sequences. Each of the protein is linked to external resources such as Entrez Gene, Human Protein Reference Database (HPRD), UniProt and NetPath for more details. It also stores the concentration of a given protein in blood, plasma or serum which could be used for diagnostics in several diseases.
	</div></td>
      </tr>

