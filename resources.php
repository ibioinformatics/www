        <td colspan="2"><strong> Defining the 'Proteome' </strong></td>
      </tr>
      <tr>
        <td colspan="2"><div align="justify">The term &quot;<em>Proteome</em>&quot;  defines the protein complement of a genome, has become the key research frontier in the post-genomic era. With the recent advancements in genomic and proteomic techniques, enormous amount of information available in literature about protein features such as protein-protein interactions, posttranslational modifications, sub-cellular localization, etc. Efforts such as fetching such information, storing them in a resource and disseminate them in easy to access web-based portals ease the task of reading literature for the scienctific researchers. To improve the efficiency of biological discoveries, Institute of Bioinformatics has developed various databases to identify and analyze protein products in a cell or tissue. These resources are freely available over the World Wide Web and can be easily accessed by the scientific community.</div>        </td>
