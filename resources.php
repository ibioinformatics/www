        <td><div align="justify"><strong>NetPath</strong> (<a href="http://www.netpath.org" target="_blank">http://www.netpath.org</a>)
            and <strong>NetSlim</strong> (<a href="http://www.netpath.org/netslim/" target="_blank">http://www.netpath.org/netslim/</a>) NetPath is a resource to store manually curated signal transduction pathways in humans, that at present stores 37 signaling pathways. These pathways were manually curated by reading published literature and made publically available after publishing them in peer reviewed journals. NetSlim represents a subset of reactions depicted in NetPath and was developed by applying a set of stringent criteria to generate high-confidence signaling maps and also to enable easy visualization and interpretation of the pathways.
	</div></td>
      </tr>
	<tr><td width="4%">&nbsp;</td><td width="96%">&nbsp;</td></tr>
