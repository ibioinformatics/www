		<td align="right">&nbsp;</td>
		<td align="right">
			<div align="justify">
				<p>Acromegaly is caused by excessive growth hormone production, most often due to pituitary adenomas. Characterized by acral growth, the disease manifests insidiously in adults and can be potentially life-threatening. In growing children, the disease presentation is dramatic with disproportionate linear growth leading to gigantism. Inaccessibility of the gland for biopsy, lack of functional cell lines and tumor heterogeneity has constrained the elucidation of molecular etiology of the disease. In collaboration with PGIMER, Chandigarh, we initiated whole-exome sequencing (WES) studies to identify genetic basis of acromegaly in Indian patients, which will aid in clinical assessment of pathogenecity. In familial cases, genetic studies are carried out and the clinician is informed of the risk factors to facilitate genetic counseling to the family.</p>
