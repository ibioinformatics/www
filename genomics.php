	<tr>
		<td align="center"><img src="images/Mycobacteriumtuberculosis.png" width="143px" height="89px"  style="padding: 4px; border: 1px solid #000000;"></td>
		<td align="right">&nbsp;</td>
		<td align="right">
			<div align="justify">
				India has the highest prevalence of TB, but the genetic diversity of <i>Mycobacterium tuberculosis</i> 
in India still remains largely unknown. Mutations in the <i>Mycobacterium tuberculosis</i> genome arise spontaneously and are the main cause of resistance towards the anti-TB drugs. Controlling TB transmission relies on accurate and rapid diagnosis. Culture-based drug susceptibility testing which is currently gold standard for TB diagnosis is laborious, time consuming and requires sophisticated lab settings and trained personnel, making it less desirable. Although major efforts are being done to increase case detection, one third of new TB cases are still missed due to non-availability of rapid, low-cost, and accurate diagnostic facilities in high-TB-burden countries. Limited knowledge about clinical strains in terms of whole genome profile in Indian context impedes researchers to conclusively explain the basis of virulence and drug resistance prevalent in India. Whole Genome Sequencing platform for analyzing clinical isolates will aid in identification of mutations associated with drug resistance. Institute of Bioinformatics, Bangalore in collaboration with National JALMA Institute of Leprosy and Other Mycobacterial Diseases, Agra, PGIMER, Chandigarh and NIMHANS, Bangalore is working towards identification of relevant mutations responsible for drug resistance TB using next generation sequencing platform.
