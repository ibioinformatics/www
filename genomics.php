	<tr>
		<td align="center"><img src="images/2.png" width="143px" height="89px" style="padding: 4px; border: 1px solid #000000;"></td>
		<td align="right">&nbsp;</td>
		<td align="right">
			<div align="justify">
				<p>Hearing impairment is one of the most frequent sensory defects and can be classified into congenital and acquired. Congenital hearing loss can be caused by hereditary and non-hereditary genetic factors. The identification of the causal hereditary mutation leading to congenital hearing impairment is important for early diagnosis, clinical follow-up and genetic counseling. India is a country with high ethnic diversity and high degree of consanguinity in some pockets. In collaboration with Command Hospital, Bangalore, we initiated a whole-exome sequencing project to identify causative novel variants/genes in this genetically heterogeneous recessive disease.</p>
			</div>
