	<tr>
		<td align="center"><img src="images/xlinked.png" width="143px" height="89px" style="padding: 4px; border: 1px solid #000000;"></td>
		<td align="right">&nbsp;</td>
		<td align="right">
			<div align="justify">
			<p>Intellectual disability (ID) previously referred to as mental retardation (MR) is a neuro developmental disorder with significant limitations in intellectual functioning and adaptive behavior. XLID is a clinically and genetically heterogeneous disorder, and exists in both syndromic and non-syndromic forms. Family based association studies have identified more than 100 genes known to be implicated in XLID. However, the cause of the disease is still unknown for the non-syndromic form of XLID and is poorly studied. In collaboration with Dr. Girimaji from NIMHANS, we collected samples from more than 40 families with more than 80 individuals affected with moderate to severe ID. Due to genetic variability of XLID, cytogenetic tests followed by array comparative genomic hybridization were performed to investigate copy number alterations and larger chromosomal deletions/duplications in all the families. Whole-exome sequencing was initiated to identify novel variants/genes involved in intellectual disability.</p>
			</div>
		</td><td>&nbsp;</td>
	</tr>

