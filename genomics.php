	<tr><td align="right">&nbsp;</td><td align="right">&nbsp;</td><td align="left"><strong>Genomic analysis of Primary Immune Deficiency diseases</strong></td><td>&nbsp;</td></tr>
	<tr>
		<td align="center"><img src="images/3.png" width="143px" height="89px" style="padding: 4px; border: 1px solid #000000;"></td>
		<td align="right">&nbsp;</td>
		<td align="right">
			<div align="justify">
				<p>Genetic diagnosis of primary immune deficiency (PID) diseases in India is subject to numerous obstacles in terms of evaluation, characterization and turnaround time for definitive diagnosis and treatment. Few established centers in the country provide comprehensive genetic and prenatal diagnosis for primary immune deficiencies. Additionally, a diverse range of infections endemic to our country and nutritional deficiencies serve to further complicate PID diagnosis. Obtaining a specific molecular diagnosis has significant implications for patient management, including genetic counseling and providing prenatal diagnosis. Therefore, it becomes important to establish facilities for quicker diagnosis of the underlying genetic defect. Higher degree of consanguinity and ethnic diversity in India also necessitates the need for molecular confirmation of the genetic players suspected to be related to a particular PID case. In collaboration with National Institute of Immunohematology in Mumbai, we have now initiated studies to identify the variants/genes involved in disease pathogenecity.</p>
			</div>
		</td><td>&nbsp;</td>
	</tr>

