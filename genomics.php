	<tr><td align="right">&nbsp;</td><td align="right">&nbsp;</td><td align="left"><strong>mRNA and genome copy number profiling of cancers and other human diseases </strong></td><td>&nbsp;</td></tr>
	<tr>
		<td align="center"><img src="images/Heatmap_Main_B_tr.gif" style="padding: 4px; border: 1px solid #000000;"></td>
		<td align="right">&nbsp;</td>
		<td align="right">
			<div align="justify">
				<p>
				Esophageal cancer is the third leading cancer in males and fourth leading in females in India, but not much
                        is known about the molecular etiology of the disease. We have used DNA microarray technology for high throughput
                        identification of molecular gene expression profiles. We observed 881 genes significantly upregulated in ESCC
                        samples as compared to the adjacent normal epithelium. The expression of two previously unreported overexpressed
                        genes, <em>ORAOV2</em> and <em>FAP</em>, was validated at the protein level by immunohistochemical labeling
                        of tissue microarrays and archival tissue sections. Overall, using this approach, we have identified a number of promising novel candidates that can be validated further for their potential to serve as biomarkers for ESCC. This is a collaborative effort between IOB, Kidwai Memorial Institute of Oncology and Johns Hopkins School of Medicine. Similarly, we are working on copy number variations in ESCC using arrayCGH.
				</p>
				<p>
				Similar approaches are adopted for diverse cancers such as pancreatic cancer, gastric adenocarcinoma, gall bladder carcinoma, hepatocellular carcinoma, leukoplakia and oral squamous cell carcinoma, cervical cancer and retinoblastoma. Other diseases for which transcriptomic profiling are being done include temporal lobe epilepsy, tuberculous meningitis, rheumatoid arthritis, osteoarthritis, spondyloarthropathy and reactive arthritis.&nbsp; We also use this technology to identify subset of genes which are responsible for the development of skin lesions in arsenic exposed patients.
				</p>
