                   <td width="693" colspan="-1" align="left" valign="top"><p align="justify">Institute of Bioinformatics, Bangalore has been collaborating with  NIMHANS to utilize cutting edge genomic and proteomic platforms for molecular  profiling of neuroinfections and neurological diseases. Currently,  investigations on different form of chronic meningitis, rabies, stroke,  epilepsy, amyloid lateral sclerosis and X-linked mental retardation are being  pursued. In the year 2008,&nbsp; IOB and NIMHANS have secured a joint research  project- &ldquo;DBT Programme Support on Neuroproteomics for Proteomic Investigation  of Neurological Disorders&rdquo;, which is funded by Department of Biotechnology,  Government of India. To enable free and accelerated interaction between NIMHANS  clinicians and IOB scientists, NIMHANS has allocated a &lsquo;Bioinformatics and  Proteomics&rsquo; laboratory to IOB in the Neurobiology Research Centre at NIMHANS.  Currently, there are more than 30 collaborative research projects in progress,  where genomic, proteomic and bioinformatics expertise of IOB is being applied  to investigate molecular level variations among various human diseases that are  of relevant to India.</p>
