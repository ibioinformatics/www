                        compendium of molecular alterations in PID, named Resource
                        of Asian Primary Immunodeficiency Diseases (RAPID) in
                        collaboration with RIKEN, Japan. It hosts information
                        on sequence variations and expression at the mRNA and
                        protein levels of all genes reported to be involved in
                        PID patients. The main objective of this database is
                        to provide detailed information pertaining to genes and
                        proteins involved in primary immunodeficiency diseases
                        along with other relevant information about protein-protein
                        interactions, mouse studies and microarray gene-expression
                        profiles in various organs and cells of the immune system.
                        RAPID also contains list of candidate PID genes predicted
                        using support vector machine learning approach.Thus,
                        information contained in this database should help physicians
                        and other biomedical investigators to further investigate
                        the role of these molecules in PID.</p>
