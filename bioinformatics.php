                  <td valign="top" class="header"><p align="justify"><a name="pathways"></a><a href="http://netpath.org/"><span class="header">NETPATH</span></a></p>
                      <p align="justify">'NetPath' is a curated resource of signal
                        transduction pathways in humans. It is a collaboration
                        between the PandeyLab at Johns Hopkins University and
                        the Institute of Bioinformatics. At this time, 10 cancer
                        and 10 immune signaling pathways are available. For example
                        Transforming growth factor-beta (TGF-beta) is a multifunctional
                        growth factor with a wide range of biological activities
                        such as cell differentiation, cell proliferation, apoptosis
                        and tumor suppression. TGF-beta signalling involves a
                        direct pathway from the cell surface receptors to the
                        nucleus. TGF- beta, binding to its receptors initiates
                        the degradation of several key components of its signaling
                        pathway. The degradation of these components, including
                        both positive and negative transducers, is mediated by
                        the ubiquitinated proteasome system. Recent cellular,
                        biochemical, and structural studies have shown significant
                        insight into the mechanisms of the activation of TGF-beta
                        receptors through ligand binding, the activation of Smad
                        proteins through phosphorylation, the transcriptional
                        regulation of target gene expression, and the control
                        of Smad protein activity and degradation.</p>
