                  <td><p><span class="header">Proteomics of Body Fluids</span></p>
                      <p align="justify">The main aim of this research is to generate and quantitatively identify the complete proteomic 		profiles of every protein expressed in various human body fluids using our state-of-the-art and highly sensitive mass spectroscopy-based proteomic techniques. We summarize some of the strategies currently being used for global identification and quantification of body fluid proteins with applications for a wide variety of human diseases. Our findings reveal novel protein species in both healthy and disease states that were not previously identified. While it is reasonable to expect that some of the same proteins are found in multiple body fluids, the primary objective of this research will be to identify a definable subset of proteins that were unique to or diagnostic for a single body fluid. The study will evaluate the entire complement of proteins in each proteome for potential utility as bodily fluid specific biomarkers. Consequently we hope that some of these biomarkers can be further characterized for use in highly sensitive diagnostic tests or for evaluating therapeutic response. With the advances made in proteomics technologies, the impact of this analysis in the search for clinically relevant disease biomarkers would be realized in the future. </p>
