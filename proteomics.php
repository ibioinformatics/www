                  <td valign="top" class="header"><p><span class="header">Glycoproteomics</span></p>
                      <p align="justify">We are using glycoproteome enrichment
                        techniques in order to detect usually less abundant glycosylated
                        proteins that are more relevant from the point of view
                        of biomarker development<strong>.</strong> We have substantial
                        experience in enrichment of glycoproteins using lectin
                        affinity chromatography. The enrichment is carried out
                        using a mixture of three lectins - concanavalin A, wheat
                        germ agglutinin and jacalin- that are immobilized on
                        a matrix.&nbsp; Use of 3 lectins provides a broader coverage
                        of glycoproteins because of their broader binding profile
                        and can provide up to 1,000 fold enrichment of molecules
                        in a single step. For identification of N-linked glycopeptides,
                        the extracted tryptic peptides are treated with PNGaseF.&nbsp; This
                        digestion step removes the N-linked glycan and is recognized
                        by a deamidation at the glycan linked asparagine during
                        cleavage. This converts asparagine to aspartic acid increasing
                        the mass by 1Da that could be very easily identified
                        by mass spectrometry.</p>
